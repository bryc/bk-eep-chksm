unsigned long long rarechksm(unsigned char data[], int size, int type) {
    unsigned long long cksm0 = 0, cksm1 = 0, sum = 0x13108B3C1;
    int bp, sd = 0;

    for(bp = 0; bp < size; bp++, sd += 7, cksm0 ^= sum) {
        sum  += data[bp] << (sd & 0xF);
        sum  = ((sum >> 1 | sum << 32) ^ (sum << 44) >> 32) & 0x1FFFFFFFF;
        sum ^= (sum >> 20) & 0xFFF;
    }
    for(bp--; bp >= 0; bp--, sd += 3, cksm1 ^= sum) {
        sum  += data[bp] << (sd & 0xF);
        sum  = ((sum >> 1 | sum << 32) ^ (sum << 44) >> 32) & 0x1FFFFFFFF;
        sum ^= (sum >> 20) & 0xFFF;
    }
    switch(type) {
        case 0: sum = (cksm0 << 32) | (cksm1 & 0xFFFFFFFF); break;
        case 1: sum = (unsigned int) (cksm0 ^ cksm1); break;
        case 2: sum = (unsigned int) (cksm0 << 16) | (cksm1 & 0xFFFF); break;
    }
    return sum;
}

